print(*a)

"""------------------------------------------------------------------------------------------"""

"""permutations()- это почти тоже самое что и product(), но отличаетсь тем что порядок важен,
   Пример: product('ABC', 2)- Выведет: АБ, АС, BC, -для него АБ и БА -это одно и тоже,
   когда permutations('ABC', 2)- Выведет: AB, BA, AC, CA, BC, CB- порядок важен."""

parts = ["gleb", "semenenko", "0", "4"]
for p in permutations(parts):
    print("".join(p))

"""------------------------------------------------------------------------------------------"""

"""accumulate()- выполняет последовательное накопление результата операции над элементами списка.
   По умолчанию — сложение, но операцию можно переопределить."""
numbers = [1, 2, 3, 4, 6, 8, 0, 10]
print(*accumulate(numbers, lambda a, b: a * b))